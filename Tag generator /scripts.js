// Function to generate styled text
function generateStyledText() {
    const userInput = document.getElementById('userInput').value.trim();
    const fontSelector = document.getElementById('fontSelector').value;
    const styledTextContainer = document.getElementById('styledTextContainer');

    if (userInput === '') {
        styledTextContainer.innerHTML = '<p>Please enter some text.</p>';
        return;
    }

    const styleObj = styles[fontSelector];
    if (!styleObj) {
        styledTextContainer.innerHTML = '<p>Selected style not available.</p>';
        return;
    }

    const transformedText = styleObj.transform(userInput);

    styledTextContainer.innerHTML = `
        <div class="styled-text">
            <p>${transformedText}</p>
            <button class="copy-btn" onclick="copyText('${transformedText}')">Copy</button>
        </div>
    `;
}

// Function to copy text to clipboard
function copyText(text) {
    navigator.clipboard.writeText(text).then(() => {
        alert('Text copied to clipboard!');
    }).catch(() => {
        alert('Failed to copy text.');
    });
}

// Event listener for generate button
document.getElementById('generateBtn').addEventListener('click', generateStyledText);
